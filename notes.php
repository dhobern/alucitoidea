            <h2>Catalogue of the Alucitoidea of the World, v1.0</h2>

            <br>
            <h3>Notes on the catalogue</h3>

            <br>
            <h4>Tineodidae</h4>
            
            <p>The family Alucitidae was previously considered a sister group to
                the Tineodidae or false plume moths but has been shown to be
                nested within them (<a href="https://doi.org/10.1186%2Fs12862-015-0520-0" target="_blank">Heikkilä et al. 2015 <i class="fas fa-external-link-alt fa-sm"></i></a>).</p>
            
            <p>This version of the Catalogue
                therefore treats these as a single family. No attempt has been
                made to place the genera in anything other than alphabetical
                order.</p>
            
            <p>A phylogenetically appropriate revision of the expanded 
                Alucitidae into subfamilies, and perhaps tribes, remains 
                necessary.</p>
            
            <br>
            <h4><em>Alucita parca</em> Rebel, 1901</h4>
            
            <p>The <a href="https://www.researchgate.net/publication/270723918" target="_blank">2003 
                catalogue</a> included <em>Alucita parca</em> on page 114 
                with the following explanation: </p>
            
            <blockquote><em>Orneodes parca</em> Rebel, 1901: Manuscript name 
                (teste Scholz & Jäckh, 1994). &mdash; Israel (teste Amsel, 1935). 
                DISTRIBUTION. Palaearctic: Israel.</blockquote>
            
            <p>It is not clear why a manuscript name was included. The name in
                question would render the otherwise valid pterophorid name 
                <a href="https://www.biodiversitylibrary.org/page/9808843" target="_blank">
                <em>Alucita parca</em> Meyrick, 1929</a> (from Haifa, Palestine)
                a homonym. The close geographic proximity of the locations is
                suggestive of some possible confusion.</p>
            
            <p>In the absence of more information, this name has been excluded 
                from the current version of the Catalogue of World Alucitoidea.</p>
            
            <br>
            <h4><em>Alucita xanthozona</em> (Clarke, 1896)</h4>
            
            <p><em>Orneodes xanthozona</em> Clarke, 1986, described from the Marquesas
                Islands, is a junior homonym of <em>Orneodes xanthozona</em>
                Diakonoff, 1954, from Irian Jaya. Both species are now included
                in <em>Alucita</em>.</p>
            
            <p>No replacement has been published for Clarke's species. This
                remains as a task for a future taxonomist working on the 
                fauna of French Polynesia.</p>

